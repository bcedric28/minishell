#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int size;
	int i;

	size = ft_strlen(dest);
	i = 0;
	while (src[i] != '\0')
	{
		dest[size + i] = src[i];
		i++;
	}
	dest[size + i] = '\0';
	return (dest);
}

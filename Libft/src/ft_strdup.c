#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		i;

	if (src == NULL)
		return (NULL);
	copy = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (copy == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
